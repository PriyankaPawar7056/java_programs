package Test;

public class Relational_operator {

	public static void main(String[] args) {
		//Relational Operator (> , < ,>= , <= , == , != )
		int a = 50 ;
		int b = 70 ;
		
		System.out.println(a>b);  //False
		System.out.println(a<b);  //True
		
		System.out.println(a>=b);  //False
		System.out.println(a<=b);  //True
		
		System.out.println(a==b);  //False
		System.out.println(a!=b);  //True
		
		
		

	}

}
