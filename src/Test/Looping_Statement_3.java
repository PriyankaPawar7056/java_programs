package Test;

public class Looping_Statement_3 {

	public static void main(String[] args) {
		
		//It is a type of CONTROL STATEMENT
				//(2) Looping Statement
				//(c) Do_____while condition
			
		int a = 20;
		
		  do {
		    a++;   //post increment
            System.out.println("true");
            
		  }while(a<=25) ;   //20 21 22 23 24 25
                           //Continuous post increment done until it found < 25 value
		                   // if while condition is false then only do code is executed in one time
                          
            
	}

}
