package Test;

public class Looping_Statement_1 {

	public static void main(String[] args) {
		
		//It is a type of CONTROL STATEMENT
				//(2) Looping Statement
				//(a) for (initialization;condition;++/--(increment and decrement))
		
		for (int a=1;a<5;a++) {        //increment example
			System.out.println(a);
		}
		
		System.out.println("next example");
		
		for (int r=60;r!=55;r--) {     //decrement example
			System.out.println(r);
		}
		
		System.out.println("end of the program");
	}
	
}
