package Test;

public class Jump_Statement_continue {

	public static void main(String[] args) {
		
		//It is a type of CONTROL STATEMENT
		//(3) Jump Statement
		//(b)Continue

		for (int a=5;a<=10;a++) {
			
			if(a==7) {                //5 6 8 9 10 test 
			continue;	              //in continue jump statement it skip particular case/iteration (EX..a==7 (7 skip))
		}
			
			System.out.println(a);
		}
		
		System.out.println("test");
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
