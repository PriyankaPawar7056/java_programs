package Test;

public class Jump_Statement_return {

	public static void main(String[] args) {
		
		
		//It is a type of CONTROL STATEMENT
		//(3) Jump Statement
		//(c)return
		
		for (int a=5;a!=10;--a) {

		if (a==2) {                  //5 4 3 
			return;                 //it directly return from particular case EX.a==2 and also not print after that anything
			
		}
		
		System.out.println(a);
		}
		
		System.out.println("thank you!");
		
		
		
		
		
		
		
		
		
		
		
	}

}
